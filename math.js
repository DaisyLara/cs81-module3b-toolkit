// Start by writing a simple function, like double(n).
function double(n) {
    return n * 2
}
console.log(double(12))

function square(n) {
    return n * n
    
}
console.log(square(7))

function isEven(n) {
    return n === Number.isEven
}
console.log(isEven(7))

function isOdd(n) {
    return n === Number.isInteger
}
console.log(isOdd(7))

function multiply(n) {
    return n * 5
}
console.log(multiply(5))
